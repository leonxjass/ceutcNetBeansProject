        System.out.println("cinco estrellas de pálido azul;");
        System.out.println("en tu emblema, que un mar rumoroso");
        System.out.println("con sus ondas bravías escuda,");
        System.out.println("de un volcán, tras la cima desnuda");
        System.out.println("hay un astro de nítida luz.");
        

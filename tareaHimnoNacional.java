        System.out.println("el audaz navegante te halló;");
        System.out.println("y al mirar tu belleza extasiado,");
        System.out.println("al influjo ideal de tu encanto,");
        System.out.println("la orla azul de tu espléndido manto");
        System.out.println("con su beso de amor consagró.");
        
        System.out.println("II");
        System.out.println("De un país donde el sol se levanta,");
